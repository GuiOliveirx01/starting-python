# Exibe uma mensagem na tela usando a função print()
print("Hello Python world!")

# Armazena a mensagem em uma variável e exibe a mensagem na tela
message = "Hello Python world!"
print(message)

# Atualiza o valor da variável com uma nova mensagem e exibe o novo conteúdo
message = "Hello Python Crash Course world!"
print(message)

# Evita um erro de nome ao escrever o segundo 's' em 'print(message)'
