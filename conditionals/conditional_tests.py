# Teste de igualdade e não igualdade
car = 'ford'

print(car == 'ford')
print(car != 'ford')

# Teste para ignorar letras maiúsculas e minúsculas
car = 'Porsche'

print(car.lower() == 'porsche')
print(car == 'porsche')

